export const ALL_WRITING_PROMPTS: WritingPrompt[] = [
  ...STORY_NARRATION_PROMPTS,
  ...EMAIL_RESPONSE_PROMPTS,
];

export function getWritingPrompt(id: string): WritingPrompt | undefined {
  return ALL_WRITING_PROMPTS.find((p) => p.id === id);
}

export function getRandomWritingPrompt(type?: WritingPromptType): WritingPrompt {
  const pool = type
    ? ALL_WRITING_PROMPTS.filter((p) => p.type === type)
    : ALL_WRITING_PROMPTS;
  return pool[Math.floor(Math.random() * pool.length)];
}
